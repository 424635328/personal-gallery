import * as React from "react"
import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    data-slot="card"
    // cn() 函数会合并所有 class
    // 1. 保留了原有的 class 逻辑
    // 2. 添加了我们的玻璃拟态样式
    className={cn(
      // --- 新增的玻璃拟态样式 ---
      "bg-black/20",                 // 20% 不透明度的黑色背景
      "backdrop-blur-lg",            // 对背景内容应用一个大的模糊效果
      "border border-white/10",      // 10% 不透明度的白色边框，营造高光效果
      "shadow-xl",                   // 添加一个明显的阴影，增加立体感
      
      // --- 保留的原有样式 ---
      "text-card-foreground",
      "flex flex-col gap-6 rounded-xl py-6", // 保留了圆角、内边距等

      // 允许从外部传入额外的 class
      className
    )}
    {...props}
  />
));
Card.displayName = "Card";

